// The test.gif fixture was already generated by gif_decoder_test.
// If it doesn't exist, generate it:
fn ensure_test_gif() {
    let path = fixture_path();
    if path.exists() {
        return;
    }
    std::fs::create_dir_all(path.parent().unwrap()).unwrap();
    let mut encoder =
        gif::Encoder::new(std::fs::File::create(&path).unwrap(), 10, 10, &[]).unwrap();
    encoder.set_repeat(gif::Repeat::Infinite).unwrap();
    for i in 0u8..3 {
        let pixels: Vec<u8> = (0..100).flat_map(|_| [i * 80, 0, 0, 255]).collect();
        let mut frame = gif::Frame::from_rgba(10, 10, &mut pixels.clone());
        frame.delay = 10;
        encoder.write_frame(&frame).unwrap();
    }
}

